void main(List<String> args) {
  int sayi1 = 10, sayi2 = 5;
  if (sayi1 == sayi2 && sayi1 > sayi2) {
    print("Sayılar Eşit ve Sayı1 Sayı2'den Büyük");
  } else {
    print("Sayılar Eşit Değil ve Sayı1 Sayı2'den Küçük");
  }
}

